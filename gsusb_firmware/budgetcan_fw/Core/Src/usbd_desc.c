/**
  * @brief  Convert Hex 32Bits value into char
  * @param  value: value to convert
  * @param  pbuf: pointer to the buffer
  * @param  len: buffer length
  * @retval None
  */
static void IntToUnicode(char * pbuf, uint32_t value)
{
  char *p = pbuf + 8;

  *p-- = 0;

  while (p >= pbuf) {
    uint8_t nybble = value & 0x0F;

    if (nybble < 10)
      *p = '0' + nybble;
    else
      *p = 'A' + nybble - 10;

    value >>= 4;
    p--;
  }
}
